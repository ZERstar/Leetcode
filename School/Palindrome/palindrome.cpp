class Solution
{
	public:
		string is_palindrome(int n)
		{
		    // Code here.
		    string num = to_string(n);
		    int left = 0;
		    int right = num.length()-1;
		    while(left<=right){
		        if(num[left]!=num[right]) return "No";
		        left++;
		        right--;
		    }
		    return "Yes";
		}
};

